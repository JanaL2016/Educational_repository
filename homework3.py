MyString001 = 'это моя строка'
print(MyString001[0])    # Вывод первого символа строки
print(MyString001[-1])   # Вывод последнего символа строки
print(MyString001[2:5])  # Вывод подстроки с третьего по пятый символы
print(MyString001[::-1]) # Вывод строки наоборот
print('Дляина строки =', len(MyString001)) # Вывод длины строки
print('это новая строка' + MyString001) # Вывод объединения строк